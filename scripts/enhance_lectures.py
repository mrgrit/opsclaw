def inject_glossary_and_enhance(filepath, course):
    """파일을 읽고 용어 해설 + 특화 내용을 주입."""
    with open(filepath, "r") as f:
        content = f.read()

    lines = content.split("\n")
    original_lines = len(lines)

    # 이미 "용어 해설" 섹션이 있으면 스킵
    if "## 용어 해설" in content:
        return original_lines, False

    glossary = GLOSSARY.get(course, "")
    if not glossary:
        return original_lines, False

    # "# 본 강의 내용" 바로 앞에 용어 해설 삽입
    insert_point = content.find("# 본 강의 내용")
    if insert_point == -1:
        # 템플릿이 아닌 수동 작성 파일 — 마지막에 추가
        content += glossary
    else:
        content = content[:insert_point] + glossary + "\n\n" + content[insert_point:]

    # 제너릭 심화 섹션의 플레이스홀더를 과목 특화로 교체
    # "#### 개념 1: 동작 원리" 등의 제너릭 내용에 과목 정보 주입
    content = content.replace(
        "#### 개념 1: 동작 원리\n\n기술적 동작 방식을 단계별로 분해하여 이해한다.",
        f"#### 개념 1: 동작 원리 ({course.replace('course', 'Course ').split('-')[0]})\n\n이 주차에서 다루는 기술의 동작 방식을 단계별로 분해하여 이해한다."
    )

    with open(filepath, "w") as f:
        f.write(content)

    new_lines = len(content.split("\n"))
    return new_lines, True


def main():
    total_enhanced = 0
    total_skipped = 0

    for course in GLOSSARY:
        for week_num in range(1, 16):
            week_dir = f"week{week_num:02d}"
            filepath = os.path.join(DST_BASE, course, week_dir, "lecture.md")

            if not os.path.exists(filepath):
                continue

            lines, enhanced = inject_glossary_and_enhance(filepath, course)
            if enhanced:
                total_enhanced += 1
                print(f"  ✓ {course}/{week_dir}: {lines}L")
            else:
                total_skipped += 1

    print(f"\nDone: {total_enhanced} enhanced, {total_skipped} skipped")


if __name__ == "__main__":
    main()
